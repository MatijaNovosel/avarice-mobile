String parseTag(int id) {
  switch (id) {
    case 1:
      return "Food";
    case 2:
      return "Gifts";
    case 3:
      return "Games";
    case 4:
      return "Public transport";
    case 5:
      return "Other";
    case 6:
      return "Drink";
    case 7:
      return "Groceries";
    default:
      return "Undefined";
  }
}
